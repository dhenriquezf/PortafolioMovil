package henriquez.daniel.prueba.app.com.login.Clases;

public class Perfil {

    public int id;
    public String tipo;

    public Perfil(int id, String tipo) {
        this.id = id;
        this.tipo = tipo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}
